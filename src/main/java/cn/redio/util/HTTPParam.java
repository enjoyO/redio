package cn.redio.util;

/**
 * Created by XIAOYAO on 2016/10/21.
 * http请求参数
 */
public class HTTPParam {

    /**
     * 请求参数
     */
    private String key;

    /**
     * 参数值
     */
    private String value;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
